import java.util.*;
class Even_or_odd
{
public static void main(String arg[])
{
int n;
Scanner sc=  new Scanner(System.in);
System.out.println("ENTER THE NUMBER");
n=sc.nextInt();
if(n%2==0)
{
System.out.println("GIVEN NUMBER IS EVEN NUMBER");
}
else
{
System.out.println("GIVEN NUMBER IS ODD NUMBER");
}
}
}
